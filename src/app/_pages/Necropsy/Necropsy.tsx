const Necropsy = () => {
  const [activeSection, setActiveSection] = useState<SectionId>("safety");
  const [expandedStep, setExpandedStep] = useState<string | null>(null);

  const current = sections.find((section) => section.id === activeSection)!;

  return (
    <main className="necropsy-page">
      <div className="necropsy-header">
        <h1>☠️ Некропсія / розтин кролика</h1>
        <p>Як самостійно провести розтин і що шукати</p>
      </div>

      <div className="necropsy-wrap">
        <div className="necropsy-alert">
          ⚠️ Розтин не заміняє лабораторну діагностику, але допомагає швидко
          зрозуміти можливу причину загибелі та ухвалити правильне рішення щодо
          решти стада.
        </div>

        <div className="necropsy-nav">
          {sections.map((section) => (
            <button
              key={section.id}
              className={`necropsy-nav-btn ${
                activeSection === section.id ? "active" : ""
              }`}
              onClick={() => {
                setActiveSection(section.id);
                setExpandedStep(null);
              }}
            >
              <span>{section.icon}</span>
              <span>{section.title}</span>
            </button>
          ))}
        </div>

        <div className="necropsy-section-header">
          <span className="necropsy-section-icon">{current.icon}</span>
          <div>
            <h2>{current.title}</h2>
            <p>{current.desc}</p>
          </div>
        </div>

        <div className="necropsy-steps">
          {current.steps.map((step, index) => {
            const key = `${activeSection}-${index}`;
            const isOpen = expandedStep === key;

            return (
              <div
                key={key}
                className={`necropsy-step ${isOpen ? "open" : ""}`}
                onClick={() => setExpandedStep(isOpen ? null : key)}
              >
                <div className="necropsy-step-top">
                  <span className="necropsy-step-num">{index + 1}</span>
                  <span className="necropsy-step-icon">{step.icon}</span>
                  <span className="necropsy-step-title">{step.title}</span>
                  <span>{isOpen ? "▲" : "▼"}</span>
                </div>

                {isOpen && (
                  <div className="necropsy-step-body">
                    <p>{step.text}</p>

                    {step.warning && (
                      <div className="necropsy-warning">⚠️ {step.warning}</div>
                    )}

                    {step.tip && (
                      <div className="necropsy-tip">💡 {step.tip}</div>
                    )}
                  </div>
                )}
              </div>
            );
          })}
        </div>

        <div className="necropsy-related">
          <h3 className="necropsy-related-title">Читайте також</h3>
          <div className="necropsy-related-grid">
            <Link href="/lab-diagnostics" className="necropsy-related-link">
              🧪 Лабораторна діагностика
            </Link>
            <Link href="/diseases" className="necropsy-related-link">
              🩺 Хвороби
            </Link>
            <Link href="/vaccinations" className="necropsy-related-link">
              💉 Вакцинація
            </Link>
            <Link href="/biosecurity" className="necropsy-related-link">
              🛡️ Біобезпека та карантин
            </Link>
            <Link href="/disinfection" className="necropsy-related-link">
              🧴 Дезінфекція
            </Link>
          </div>
        </div>

        <div className="necropsy-back">
          <Link href="/" className="necropsy-back-btn">
            ⬅ На головну
          </Link>
          <ShareButton title="Назва цієї сторінки" />
        </div>
      </div>
    </main>
  );
};

export default Necropsy;
