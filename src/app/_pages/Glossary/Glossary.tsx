const categories = ["Всі", "Розведення", "Годування", "Здоров'я", "Утримання"];

const Glossary = () => {
  const [search, setSearch] = useState("");
  const [activeCategory, setActiveCategory] = useState("Всі");
  const [openTerm, setOpenTerm] = useState<string | null>(null);

  const filtered = useMemo(() => {
    const q = search.toLowerCase().trim();
    return terms.filter((t) => {
      const matchCat =
        activeCategory === "Всі" || t.category === activeCategory;
      const matchSearch =
        !q ||
        t.term.toLowerCase().includes(q) ||
        t.short.toLowerCase().includes(q) ||
        t.definition.toLowerCase().includes(q);
      return matchCat && matchSearch;
    });
  }, [search, activeCategory]);

  const grouped = useMemo(() => {
    const map: Record<string, Term[]> = {};
    filtered.forEach((t) => {
      const letter = t.term[0].toUpperCase();
      if (!map[letter]) map[letter] = [];
      map[letter].push(t);
    });
    return Object.entries(map).sort(([a], [b]) => a.localeCompare(b, "uk"));
  }, [filtered]);

  return (
    <main className="gl-page">
      <div className="gl-header">
        <h1>📖 Словник кролівника</h1>
        <p>Усі терміни пояснені людською мовою — від А до Я</p>
      </div>

      <div className="gl-wrap">
        <div className="gl-controls">
          <div className="gl-search-box">
            <span>🔍</span>
            <input
              id="glossary-search"
              name="glossary-search"
              className="gl-search"
              type="text"
              placeholder="Пошук терміну..."
              value={search}
              onChange={(e) => setSearch(e.target.value)}
            />
            {search && (
              <button className="gl-clear" onClick={() => setSearch("")}>
                ✕
              </button>
            )}
          </div>

          <div className="gl-cats">
            {categories.map((c) => (
              <button
                key={c}
                className={`gl-cat ${activeCategory === c ? "active" : ""}`}
                onClick={() => setActiveCategory(c)}
              >
                {c}
              </button>
            ))}
          </div>
        </div>

        <div className="gl-count">
          {filtered.length}{" "}
          {filtered.length === 1
            ? "термін"
            : filtered.length < 5
              ? "терміни"
              : "термінів"}
        </div>

        {filtered.length === 0 ? (
          <div className="gl-empty">
            <span>😕</span>
            <p>Нічого не знайдено за запитом «{search}»</p>
          </div>
        ) : (
          <div className="gl-list">
            {grouped.map(([letter, group]) => (
              <div key={letter} className="gl-group">
                <div className="gl-letter">{letter}</div>
                <div className="gl-group-items">
                  {group.map((t) => {
                    const isOpen = openTerm === t.term;
                    return (
                      <div
                        key={t.term}
                        className={`gl-item ${isOpen ? "open" : ""}`}
                        onClick={() => setOpenTerm(isOpen ? null : t.term)}
                      >
                        <div className="gl-item-top">
                          <div className="gl-item-texts">
                            <span className="gl-item-term">{t.term}</span>
                            <span className="gl-item-short">{t.short}</span>
                          </div>
                          <div className="gl-item-right">
                            <span className="gl-item-cat">{t.category}</span>
                            <span className="gl-item-arrow">
                              {isOpen ? "▲" : "▼"}
                            </span>
                          </div>
                        </div>

                        {isOpen && (
                          <div className="gl-item-body">
                            <p className="gl-definition">{t.definition}</p>

                            {t.example && (
                              <div className="gl-example">
                                <span>💬</span>
                                <span>{t.example}</span>
                              </div>
                            )}

                            {t.related && t.related.length > 0 && (
                              <div className="gl-related">
                                <span className="gl-related-label">
                                  Пов'язані терміни:
                                </span>
                                <div className="gl-related-tags">
                                  {t.related.map((r) => (
                                    <span
                                      key={r}
                                      className="gl-tag"
                                      onClick={(e) => {
                                        e.stopPropagation();
                                        setSearch(r);
                                        setOpenTerm(null);
                                        setActiveCategory("Всі");
                                      }}
                                    >
                                      {r}
                                    </span>
                                  ))}
                                </div>
                              </div>
                            )}
                          </div>
                        )}
                      </div>
                    );
                  })}
                </div>
              </div>
            ))}
          </div>
        )}

        {/* ЧИТАЙТЕ ТАКОЖ */}
        <div className="gl-related-pages">
          <h3 className="gl-related-pages-title">Читайте також</h3>
          <div className="gl-related-pages-grid">
            <Link href="/beginner-guide" className="gl-related-pages-link">
              🚀 Маршрут новачка
            </Link>
            <Link href="/diseases" className="gl-related-pages-link">
              🩺 Хвороби
            </Link>
            <Link href="/okril" className="gl-related-pages-link">
              🍼 Окріл
            </Link>
            <Link href="/breeding" className="gl-related-pages-link">
              🧬 Схрещування
            </Link>
            <Link href="/economics" className="gl-related-pages-link">
              📊 Економіка господарства
            </Link>
          </div>
        </div>

        <div className="gl-back">
          <Link href="/" className="gl-back-btn">
            ⬅ На головну
          </Link>
          <ShareButton title="Назва цієї сторінки" />
        </div>
      </div>
    </main>
  );
};

export default Glossary;
